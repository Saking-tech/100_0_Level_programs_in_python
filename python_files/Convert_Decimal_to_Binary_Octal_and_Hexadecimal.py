# Program to convert decimal to binary, octal, and hexadecimal

# Input a decimal number
dec = int(input("Enter a decimal number: "))

# Convert and display the results
print(f"The binary representation of {dec} is {bin(dec)}")
print(f"The octal representation of {dec} is {oct(dec)}")
print(f"The hexadecimal representation of {dec} is {hex(dec)}")
